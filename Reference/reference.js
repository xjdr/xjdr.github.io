var CppOnMac = document.getElementById("CppOnMac");

CppOnMac.onmouseover = function() {
    CppOnMac.style.color = '#3C729D';
};

CppOnMac.onmouseout = function() {
    CppOnMac.style.color = 'black'
};

CppOnMac.addEventListener('click', function () {
    window.location.href = 'CppOnMac.html';
});

